#import "UIColor+Shopelia.h"

@implementation UIColor (Shopelia)

+(UIColor *) shopeliaBlue {
    return [UIColor colorWithRed:0 green:0.5059 blue:0.651 alpha:1.0];
}

+(UIColor *) shopeliaLightBlue {
    return [UIColor colorWithRed:0 green:0.776 blue:1.0 alpha:1.0];
}

+(UIColor *) shopeliaLightGray {
    return [UIColor colorWithRed:0.667 green:0.667 blue:0.667 alpha:1.0];
}

+(UIColor *) shopeliaBackgroundColor {
    return [UIColor colorWithRed:0.914 green:0.937 blue:0.937 alpha:1.0];
}

@end

